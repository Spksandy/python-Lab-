#Ques-22: Various actions in dictionary. 
d={1:2,3:4,5:6}
for i in d:
    print(i) # to print keys
    print(d[i]) # to print values
print()
for i in d.keys(): # to print keys
    print(i)
print()    
for i in d.values(): # to print values
    print(i)
print()
for i in d.items(): # to print items
    print(i)            
print()